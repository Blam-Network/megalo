/**
 * Bundle MegaloEdit-style docs highlighting for VitePress (no blf in the bundle).
 */
import esbuild from "esbuild";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const entry = path.join(root, "docs", ".vitepress", "megalo-code-html.ts");
const outfile = path.join(root, "docs", ".vitepress", "megalo-highlight.bundle.mjs");
const vocabularyStub = path.join(root, "docs", ".vitepress", "stubs", "vocabulary-stub.ts");
const operatorsStub = path.join(root, "docs", ".vitepress", "stubs", "operators-stub.ts");

await esbuild.build({
  entryPoints: [entry],
  outfile,
  bundle: true,
  format: "esm",
  platform: "node",
  target: "node20",
  logLevel: "info",
  plugins: [
    {
      name: "megalo-docs-stubs",
      setup(build) {
        build.onResolve({ filter: /^\.\/vocabulary$/ }, () => ({
          path: vocabularyStub,
        }));
        build.onResolve({ filter: /^\.\/operators$/ }, () => ({
          path: operatorsStub,
        }));
        build.onResolve({ filter: /@blamnetwork\/blf/ }, () => ({
          path: path.join(root, "docs", ".vitepress", "stubs", "blf-empty-stub.ts"),
        }));
      },
    },
  ],
});

const banner = "// Generated by scripts/bundle-docs-highlight.mjs — do not edit.\n";
fs.writeFileSync(outfile, banner + fs.readFileSync(outfile, "utf8"));
console.log(`Bundled docs highlighter to ${outfile}`);
